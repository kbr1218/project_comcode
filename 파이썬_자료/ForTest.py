#for

'''for문의 기본 구조
for 변수 in [리스트/튜플/문자열] :
    수행할 문장 1
    수행할 문장 2
    ...
while문과 비슷해서 서로 바꿔서 표현 가능
'''

#for문 예시
#1. 전형적인 for문
print("-" * 10, "for문 예시 : 전형적인 for문", "-" * 10)


#2. 다양한 for문의 사용
print("\n" + "-" * 10, "for문 예시 : 다양한 for문의 사용", "-" * 10)


#3. for문의 응용
print("\n" + "-" * 10, "for문의 예시 : for문의 응용", "-" * 10)



#for문과 continue : for문의 처음으로 돌아가는 continue
print("\n" + "-" * 10, "for문과 continue", "-" * 10)


#for문과 자주 사용하는 range 함수
#range(시작 숫자, 끝 숫자)  ***끝 숫자는 포함되지 않는다. ***
print("\n" + "-" * 10, "range 함수의 예시", "-" * 10)


#for문과 range를 이용한 구구단
print("\n" + "-" * 10, "for문과 range를 이용한 구구단", "-" * 10)


#리스트 내포(List comprehension) 사용하기 : 리스트 안에 for문 넣기
#[표현식 for 항목 in 반복가능 객체 if 조건문]
print("\n" + "-" * 10, "리스트 내포 사용하기", "-" * 10)

