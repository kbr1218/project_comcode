#while문 : 반복해서 문장을 수행해야 할  경우 사용

'''while문의 기본 구조
while <조건문> :
    수행할 문장 1
    수행할 문장 2
    ...
'''

#while문 예시 (treeHit)
print("\n" + "-" * 10, "while문 예시 (TreeHit)", "-" * 10)



#while문 예시 2 (여러 가지 선택지 중 하나를 선택해서 입력받는 예제)
print("\n" + "-" * 10, "while문 예시 2 (number)", "-" * 10)


#while문 강제로 빠져나가기 : break
print("\n" + "-" * 10, "while문 강제로 빠져나가기 : break를 사용한 while문 예시 3(coffee)", "-" * 10)


#while문의 맨 처음으로 돌아가기 : continue
print("\n" + "-" * 10, "continue를 사용해서 while문 맨 처음으로 돌아가기 : while문 예시 4 (홀수 출력)", "-" * 10)


#무한 루프(loop)
print("\n" + "-" * 10, "무한 루프, <ctrl + c>로 빠져나갈 수 있다.", "-" * 10)